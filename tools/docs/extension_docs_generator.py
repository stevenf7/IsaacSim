def generate_extension_custom(ext_path, ext_name, ext_config, sections, error_as_warn):
    # read index.rst
    file_path = os.path.join(ext_path, "docs", "index.rst")
    with open(file_path, "r") as file:
        src = file.readlines()
    # iterate for each custom section
    for section in sections:
        if isinstance(section, dict):
            # apply content
            custom_file_path = next(iter(section.values()))
            section_name = next(iter(section.keys()))

            # Check if the path is relative to docs or has a parent directory reference
            if custom_file_path.startswith("../"):
                # Handle paths that point outside of docs directory
                absolute_custom_file_path = os.path.normpath(os.path.join(ext_path, "docs", custom_file_path))
            else:
                # Regular path within docs directory
                absolute_custom_file_path = os.path.join(ext_path, "docs", custom_file_path)

            if os.path.isfile(absolute_custom_file_path):
                # Check if it's a Markdown file
                if custom_file_path.endswith(".md"):
                    # For Markdown files, use the myst-parser directive
                    content = f".. include:: {custom_file_path}\n   :parser: myst_parser.sphinx_"
                else:
                    # For RST files, use the standard include directive
                    content = f".. include:: {custom_file_path}"

                src = _apply_autogenerated_content(src, section_name, content)
            else:
                src = _apply_autogenerated_content(src, section_name, None)
    # write index.rst
    with open(file_path, "w") as file:
        file.writelines(src)


def setup_repo_tool(parser: argparse.ArgumentParser, config: Mapping) -> Callable:
    parser.description = """
Generate extension documentations.

Document sections
=================

The tool allows configuring how the sections are organized via the 'sections'
list (in 'repo_extension_docs' config). If not specified, the default order is:

  Section name     Description
  ---------------- --------------------------------------------------------------
  reference        Cross-referencing label. This should be always the first one!
  title            Extension name and title (from extension.toml)
  deprecation      Deprecation warning (from extension.toml)
  version          Extension version (from extension.toml)
  description      Extension description (from extension.toml, if any)
  preview          Preview image (from extension.toml, if file size > 1KB)
  enable-extension Autogenerated steps to enable the extension
  settings         Autogenerated extension settings (from extension.toml, if any)
  ogn              OmniGraph post-build generated documentation (if any)

Custom sections can also be specified as a dictionary where the key is the custom
section name and the value is the path to the .rst file with the custom content.
Path should be relative to the 'index.rst' file in the extension 'docs' folder.
Example:

  sections = [
    "reference",
    { "api": "api.rst" },
    ...
  ]

Documenting custom sections
===========================

Custom sections are included as document fragments via the 'include' directive.
Therefore, they should only use the following section headers:

  =  for sections
  -  for sub-sections
  ^  for sub-sub-sections
  "  for paragraphs

Since C++ API documentation is generated via breathe (Sphinx/Doxygen bridge) in
a unique location, mention to C++ API in extension docs should be created using
cross-referencing to extension namespace via the :any: or :ref: tags.
Example:

  Give the extension: MY.CUSTOM.EXTENSION

  * :any:`MY::CUSTOM::EXTENSION`
  * :ref:`MY::CUSTOM::EXTENSION <namespaceMY_1_1CUSTOM_1_1EXTENSION>`

CLI arguments
=============
"""
    parser.formatter_class = argparse.RawTextHelpFormatter
    parser.add_argument(
        "--no-overwrite-index",
        required=False,
        default=False,
        action="store_true",
        help=(
            "Whether not to overwrite existing 'docs/index.rst' files. "
            "Note that only content outside the autogenerated sections will not be overwritten."
        ),
    )
    parser.add_argument(
        "--error-as-warn",
        required=False,
        default=False,
        action="store_true",
        help="Whether to treat errors as warnings. If not set, execution will terminate when an error is detected.",
    )
    omni.repo.man.add_config_arg(parser)

    def run_repo_tool(options: Mapping, config: Mapping):
        # get repo config
        repo_tool_config = config["repo_extension_docs"]
        sections = repo_tool_config.get("sections", DEFAULT_SECTIONS)
        build_extension_paths = repo_tool_config["build_extension_paths"]
        source_extension_paths = repo_tool_config["source_extension_paths"]
        excluded_extensions = repo_tool_config.get("excluded_extensions", [])

        for build_extension_path in build_extension_paths:
            # resolve and check path
            build_extension_path = omni.repo.man.resolve_tokens(build_extension_path)
            if not os.path.isdir(build_extension_path):
                omni.repo.man.print_log(f"Search path ({build_extension_path}) doesn't exist", logging.WARN)
                continue
            directories = [
                item
                for item in os.listdir(build_extension_path)
                if os.path.isdir(os.path.join(build_extension_path, item))
            ]
            for ext_name in sorted(directories):
                # check for excluded extensions
                if ext_name in excluded_extensions:
                    omni.repo.man.print_log(f"[SKIPPED] {ext_name} (marked as excluded)", logging.INFO)
                    continue
                # setup extension path and config
                omni.repo.man.print_log(f"{ext_name}", logging.INFO)
                ext_path = os.path.join(build_extension_path, ext_name)
                try:
                    ext_config = _load_extension_config(ext_path, options.error_as_warn)
                except FileNotFoundError as e:
                    omni.repo.man.print_log(f"  |-- Skipping (no config/extension.toml): {e}", logging.WARN)
                    continue
                # create docs/index.rst file
                create_index(ext_path, ext_name, ext_config, sections, overwrite=not options.no_overwrite_index)
                # generate extension docs
                generate_extension_metadata(ext_path, ext_name, ext_config, options.error_as_warn)
                generate_extension_enable(ext_path, ext_name, ext_config, options.error_as_warn)
                generate_extension_settings(ext_path, ext_name, ext_config, options.error_as_warn)
                generate_extension_toctree(ext_path, ext_name, ext_config, options.error_as_warn)
                generate_extension_ogn(ext_path, ext_name, ext_config, options.error_as_warn)
                generate_extension_custom(ext_path, ext_name, ext_config, sections, options.error_as_warn)
                generate_extension_python_api(ext_path, ext_name, ext_config, options.error_as_warn)

                # included extsbuild extensions needs to be copied to docs/extsbuild
                extsbuild_exts_includes = repo_tool_config.get("extsbuild_exts_includes", [])
                if ext_name in extsbuild_exts_includes:
                    # Get the root directory by going up from source_extension_paths[0]
                    root_dir = os.path.dirname(os.path.dirname(source_extension_paths[0]))
                    destination_dir = os.path.join(root_dir, "docs", "extsbuild", ext_name)

                    copy_files_from_folder(
                        ext_path,
                        destination_dir,
                        ["*.rst", "*.toml"],
                        recursive=True,
                        preserve_structure=True,
                        overwrite=True,
                    )

    return run_repo_tool
