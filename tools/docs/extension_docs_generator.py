def _load_extension_config(ext_path: str, error_as_warn: bool) -> Mapping[str, Any]:
    with open(os.path.join(ext_path, "config", "extension.toml"), "rb") as file:
        try:
            config = tomli.load(file)
        except Exception as e:
            if error_as_warn:
                omni.repo.man.print_log(f"  |-- Wrong extension.toml format: {e}", logging.WARN)
            else:
                omni.repo.man.print_log(f"  |-- Wrong extension.toml format: {e}", logging.ERROR)
                sys.exit(1)
            return {}
    return config


def _apply_autogenerated_content(
    src: Union[str, Sequence[str]], placeholder: str, content: Union[str, Sequence[str], None]
):
    is_str = False
    if isinstance(src, str):
        src = src.split("\n")
        is_str = True
    # find placeholders
    begin = None
    begin_placeholder = f"    [begin {placeholder} autogenerated]"
    if f"{begin_placeholder}" in src:
        begin = src.index(f"{begin_placeholder}") + 1
    if f"{begin_placeholder}\n" in src:
        begin = src.index(f"{begin_placeholder}\n") + 1
    end = None
    end_placeholder = f"    [end {placeholder} autogenerated]"
    if f"{end_placeholder}" in src:
        end = src.index(f"{end_placeholder}") - 1
    if f"{end_placeholder}\n" in src:
        end = src.index(f"{end_placeholder}\n") - 1
    # check if placeholder is found
    if begin is None or end is None:
        return "\n".join(src) if is_str else src
    # erase content
    if content is None:
        dst = src[:begin] + src[end:]
        return "\n".join(dst) if is_str else dst
    # apply content
    if isinstance(content, str):
        content = content.split("\n")
    content = [""] + content + [""]
    dst = src[:begin] + [f"{item}\n" for item in content] + src[end:]
    return "\n".join(dst) if is_str else dst


def copy_files_from_folder(
    source_folder: str,
    destination_folder: str,
    file_patterns: Sequence[str] = None,
    recursive: bool = False,
    preserve_structure: bool = True,
    overwrite: bool = True,
) -> Sequence[str]:
    """Copy a set of files from one folder to another.

    This function copies files from a source folder to a destination folder based on
    specified file patterns. It supports recursive copying and can preserve or flatten
    the directory structure.

    Args:
        source_folder: Path to the source folder containing files to copy.
        destination_folder: Path to the destination folder where files will be copied.
        file_patterns: List of glob patterns to match files. If None, copies all files.
            Default patterns like ['*.rst', '*.md', '*.txt'] can be used for documentation.
        recursive: Whether to search for files recursively in subdirectories.
        preserve_structure: Whether to preserve the directory structure from source.
            If False, all files will be copied directly to destination_folder.
        overwrite: Whether to overwrite existing files in the destination.

    Returns:
        List of paths to the copied files in the destination folder.

    Raises:
        FileNotFoundError: If source_folder doesn't exist.
        OSError: If there are permission issues or other file system errors.

    Example:

    .. code-block:: python

        # Copy all .rst files from source to destination
        copied_files = copy_files_from_folder(
            "/path/to/source",
            "/path/to/destination",
            file_patterns=["*.rst"]
        )

        # Copy all documentation files recursively
        copied_files = copy_files_from_folder(
            "/path/to/source",
            "/path/to/destination",
            file_patterns=["*.rst", "*.md", "*.txt"],
            recursive=True
        )
    """
    if not os.path.exists(source_folder):
        raise FileNotFoundError(f"Source folder does not exist: {source_folder}")

    if not os.path.isdir(source_folder):
        raise ValueError(f"Source path is not a directory: {source_folder}")

    # Create destination folder if it doesn't exist
    os.makedirs(destination_folder, exist_ok=True)

    copied_files = []

    # Default file patterns for documentation if none provided
    if file_patterns is None:
        file_patterns = ["*.rst", "*.md", "*.txt", "*.png", "*.jpg", "*.jpeg", "*.gif", "*.svg"]

    # Find all matching files
    matching_files = []
    for pattern in file_patterns:
        if recursive:
            # Use rglob for recursive search
            matching_files.extend(glob.glob(os.path.join(source_folder, "**", pattern), recursive=True))
        else:
            # Use glob for non-recursive search
            matching_files.extend(glob.glob(os.path.join(source_folder, pattern)))

    # Remove duplicates and sort
    matching_files = sorted(list(set(matching_files)))

    for source_file in matching_files:
        if not os.path.isfile(source_file):
            continue

        # Determine destination path
        if preserve_structure:
            # Preserve the relative path structure
            rel_path = os.path.relpath(source_file, source_folder)
            dest_file = os.path.join(destination_folder, rel_path)
            # Create subdirectories if needed
            dest_dir = os.path.dirname(dest_file)
            if dest_dir:
                os.makedirs(dest_dir, exist_ok=True)
        else:
            # Flatten structure - copy directly to destination folder
            filename = os.path.basename(source_file)
            dest_file = os.path.join(destination_folder, filename)

        # Copy the file
        try:
            if overwrite or not os.path.exists(dest_file):
                shutil.copy2(source_file, dest_file)
                copied_files.append(dest_file)
                logger.info(f"Copied: {source_file} -> {dest_file}")
            else:
                logger.warning(f"Skipped existing file: {dest_file}")
        except Exception as e:
            logger.error(f"Failed to copy {source_file}: {e}")
            raise OSError(f"Failed to copy {source_file}: {e}")

    return copied_files


def create_index(ext_path, ext_name, ext_config, sections, overwrite):
    file_path = os.path.join(ext_path, "docs", "index.rst")
    # check if index.rst exists
    if not overwrite:
        if os.path.isfile(file_path):
            omni.repo.man.print_log(f"  |-- Skipping existing docs/index.rst", logging.INFO)
            return
    # build template
    template = """..
    This file was auto-generated by the 'repo_extension_docs' tool.
    Run 'repo extension_docs --help' for more details.
"""
    for section in sections:
        if isinstance(section, dict):
            section = next(iter(section.keys()))
        template += f"\n..\n    [begin {section} autogenerated]\n..\n    [end {section} autogenerated]\n"
    # create index.rst
    with open(file_path, "w") as file:
        file.write(template)


def generate_extension_metadata(ext_path, ext_name, ext_config, error_as_warn):
    # read index.rst
    file_path = os.path.join(ext_path, "docs", "index.rst")
    with open(file_path, "r") as file:
        src = file.readlines()
    # build and apply content
    # - reference
    content = f".. _ext_{ext_name.replace('.', '_')}:"
    src = _apply_autogenerated_content(src, "reference", content)
    # - title
    title = ext_config.get("package", {}).get("title", "")
    if not title:
        if error_as_warn:
            omni.repo.man.print_log(f"  |-- extension 'title' not filled in extension.toml", logging.WARN)
        else:
            omni.repo.man.print_log(f"  |-- extension 'title' not filled in extension.toml", logging.ERROR)
            sys.exit(1)
    content = f"[{ext_name}] {title}"
    content = f'{content}\n{"#" * len(content)}'
    src = _apply_autogenerated_content(src, "title", content)
    # - version
    version = ext_config.get("package", {}).get("version")
    if not version:
        if error_as_warn:
            omni.repo.man.print_log(f"  |-- extension 'version' not filled in extension.toml", logging.WARN)
        else:
            omni.repo.man.print_log(f"  |-- extension 'version' not filled in extension.toml", logging.ERROR)
            sys.exit(1)
    content = f"**Version**: :guilabel:`{version}`"
    src = _apply_autogenerated_content(src, "version", content if version else None)
    # - deprecation
    deprecation = ext_config.get("deprecation", {}).get("warning")
    if deprecation:
        content = f".. warning::\n    **Deprecation:** {deprecation}"
        src = _apply_autogenerated_content(src, "deprecation", content)
    else:
        src = _apply_autogenerated_content(src, "deprecation", None)
    # - description
    description = ext_config.get("package", {}).get("description")
    if not description:
        if error_as_warn:
            omni.repo.man.print_log(f"  |-- extension 'description' not filled in extension.toml", logging.WARN)
        else:
            omni.repo.man.print_log(f"  |-- extension 'description' not filled in extension.toml", logging.ERROR)
            sys.exit(1)
    if len(description.split(" ")) <= 5:
        omni.repo.man.print_log(f"  |-- Short description: {description}", logging.WARN)
    # Skip description if Overview.md exists (it would be redundant)
    has_overview = os.path.isfile(os.path.join(ext_path, "docs", "Overview.md"))
    if has_overview:
        src = _apply_autogenerated_content(src, "description", None)
    else:
        src = _apply_autogenerated_content(src, "description", description if description else None)
    # - preview
    preview_image = ext_config.get("package", {}).get("preview_image", "")
    preview_image_path = os.path.join(ext_path, preview_image)
    if os.path.isfile(preview_image_path) and os.stat(preview_image_path).st_size > 1024:  # > 1KB
        content = f"""
.. image:: ../{preview_image}
    :align: center
    :alt: Preview
"""
        src = _apply_autogenerated_content(src, "preview", content)
    else:
        src = _apply_autogenerated_content(src, "preview", None)
    # write index.rst
    with open(file_path, "w") as file:
        file.writelines(src)


