relevant_metrics = [9, 11, 18]


def get_file(name):
    files = [x for x in os.listdir(results_dir) if os.path.isfile(x) and x.endswith(".json")]
    files.sort(key=lambda x: os.path.getmtime(x), reverse=True)
    for i in files:
        if i.startswith(name) and i.endswith(".json") and len(i) == len(name) + 13:
            return i
    return None


for profile in config:
    start_text = profile["title"]
    N_counts = profile["xlabels"]
    benchmark_files = profile["plots"]
    all_measurements = dict()

    for line in benchmark_files:
        measurements = dict()
        phases = list()
        metrics = dict()

        for f_index, benchmark_file in enumerate(benchmark_files[line]):
            try:
                f = get_file(benchmark_file)
                if f is None:
                    print("File not found: " + benchmark_file)
                    continue
                with open(f, mode="r", encoding="utf-8") as jsonfile:
                    data = json.load(jsonfile)[0]
                    print(jsonfile.name)
                    test_name = data["test_name"]
                    measurements[f_index] = dict()

                    for m_index, m in enumerate(data["measurements"]):
                        phase = m["name"][len(test_name) + 1 :].split(" ")[0]
                        metric = m["name"][len(test_name) + 1 + len(phase) + 1 :]
                        metric_num = all_metrics.index(metric)
                        if phase not in measurements[f_index]:
                            measurements[f_index][phase] = dict()
                            if phase not in phases:
                                phases.append(phase)
                                metrics[phase] = dict()
                        if metric in measurements[f_index][phase]:
                            print(
                                "Duplicate metric:",
                                metric,
                                "in",
                                phase,
                                "phase of",
                                test_name,
                                "test",
                            )
                        if metric not in all_metrics:
                            print(
                                "Unknown metric:",
                                metric,
                                "in",
                                phase,
                                "phase of",
                                test_name,
                                "test",
                            )
                        elif metric_num in relevant_metrics:
                            val = m["value"]
                            if m["unit"] == "ms":
                                if metric_num in [9]:
                                    val /= 1000  # convert to seconds
                                    metrics[phase][metric] = "time (s)"
                                elif metric_num in [10, 11]:
                                    val = 1000 / val  # convert to fps
                                    metrics[phase][metric] = "fps"
                                else:
                                    metrics[phase][metric] = "ms"
                            elif metric_num == 18:
                                metrics[phase][metric] = "real time factor"
                            else:
                                metrics[phase][metric] = m["unit"]

                            measurements[f_index][phase][metric_num] = round(float(val), 2)

            except FileNotFoundError:
                print("File not found: " + benchmark_file)

        all_measurements[line] = measurements

    for phase in phases:
        for metric in metrics[phase]:
            metric_num = all_metrics.index(metric)
            try:
                plt.figure()
                plt.title(
                    start_text
                    + " "
                    + phase
                    + " "
                    + (metric if metric_num not in [10, 11] else metric.replace("Frametime", "FPS"))
                )
                plt.xlabel("N")
                plt.ylabel(metrics[phase][metric])
                for line in all_measurements:
                    measurements = all_measurements[line]
                    plt.plot(
                        range(len(measurements)),
                        [
                            measurements[x][phase][metric_num]
                            if phase in measurements[x] and metric_num in measurements[x][phase]
                            else None
                            for x in range(len(measurements))
                        ],
                        "-o",
                    )
                if len(all_measurements) > 1:
                    plt.legend(all_measurements.keys())
                plt.xticks(range(len(N_counts)), [N_counts[xi] for xi in range(len(N_counts))])
                plt.savefig(
                    start_text
                    + "_"
                    + phase
                    + "_"
                    + (metric if metric_num not in [10, 11] else metric.replace("Frametime", "FPS"))
                    + ".png"
                )
                plt.close()
            except KeyError:
                print(
                    "Error in metric:",
                    metric,
                    "in",
                    phase,
                    "phase of test",
                    start_text,
                )
