def main():
  if(os.path.isdir(args.out_dir)):
    die(f"{args.out_dir} exists: refusing to proceed")

  os.makedirs(args.out_dir)
  logging.info(f"Output root: {args.out_dir}")

  for image in args.images:
    logging.info(f"  > {image}")
    img_dir = image
    img_dir = img_dir.replace(":", "_")
    out_dir = os.path.join(args.out_dir, img_dir)
    logging.info(f"  > output to  {out_dir}")

    os.makedirs(out_dir)
    report = collect_licenses(image, out_dir=out_dir)
    if(not(len(report['license_files']))):
      empty_marker = os.path.join(out_dir, "exists")
      logging.info(f"  > no license files found for this image, placing " +
                   f"empty marker at {empty_marker}")
      with open(empty_marker, 'w') as h:
        h.write('exists')
    else:
      bug_body = gen_swipat_bug(image=image,       
                               licenses = report['license_files'],
                               packages = report['package_lists'])

      bug_out = f"{out_dir}/swipat_bug_body.txt"
      with(open(bug_out, 'w') as h):
        h.write(bug_body)
      logging.info(f"  > SWIPAT bug body written to {bug_out}")

