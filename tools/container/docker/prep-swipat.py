def collect_licenses(image, out_dir=None):
  ret = { 'license_files':[], 'package_lists': [] }
  tmp_dir = os.path.join(out_dir, "tmp")
  os.makedirs(tmp_dir)

  logging.info(f"  > copying licensing info out of the container")
  docker.copy_from_image(image, f"/omni/PACKAGE-LICENSES/", 
                         tmp_dir)

  for lic_type in ['npm', 'pip']:
    logging.info(f"  > processing {lic_type}")
    licenses_root = os.path.join(tmp_dir, "PACKAGE-LICENSES", lic_type)
    if(not os.path.isdir(licenses_root)):
      die(f"Can't locate {lic_type} licenses in the container!")

    packages_file = os.path.join(licenses_root, 'packages.txt')
    licenses_file = os.path.join(licenses_root, 'licenses.txt')
    empty_marker = os.path.join(licenses_root, 'empty')
    if(os.path.isfile(packages_file)):
      logging.info(f"    > found package list, copying")
      pkg_file_dest = f"packages.{lic_type}.txt"
      lic_file_dest = f"licenses.{lic_type}.txt"
      shutil.copyfile(packages_file, os.path.join(out_dir, pkg_file_dest))
      shutil.copyfile(licenses_file, os.path.join(out_dir, lic_file_dest))
      ret['license_files'].append(lic_file_dest)
      ret['package_lists'].append(pkg_file_dest)
    elif(os.path.isfile(empty_marker)):
      logging.info(f"    > empty marker found at {empty_marker}, skipping")
      continue
    else:
      die("Something's wrong: can't find neither package lists nor " + 
         f"empty marker in {licenses_root}")

  shutil.rmtree(tmp_dir)
  return(ret)
 

def parse_args():
  argparser = argparse.ArgumentParser(description=description)

  argparser.add_argument("images", metavar="image", nargs='+', default=None,
                         help="Images to process. @ means all last built " + 
                              "ones."
                        )

  argparser.add_argument("-o", "--out-dir", metavar="dir",
                          required=1,
                          dest="out_dir", 
                          help="Output directory")


  argparser.add_argument("-s", "--server", metavar="server",
                          default=None,
                          dest="server", 
                          help="Docker server to use (defaults to environment)")

  argparser.add_argument("--ssh", 
                          default=False, action='store_true',
                          dest="ssh", 
                          help="Use SSH to connect to Docker"
                        )

 

  global args

  args = argparser.parse_args()

def init_docker_conn():
  server = None

  if(args.ssh):
    logging.info("Using SSH to connect to server")
    server = f"ssh://{args.server}"
  else:
    logging.info("Using TCP to connect to server")
    server = args.server

  conn = nvidia.docker.Docker(server=server)
  return(conn)

